async function loadAberrantProducts() {
  console.log('Creating CVS aberrant products table...');

  // Create the table
  await pool.query(`
    CREATE TABLE IF NOT EXISTS cvs_aberrant_products (
      id SERIAL PRIMARY KEY,
      product_name TEXT NOT NULL,
      ndc VARCHAR(20) NOT NULL,
      effective_date DATE DEFAULT '2024-01-01',
      created_at TIMESTAMPTZ DEFAULT NOW(),
      UNIQUE(ndc)
    )
  `);

  // Create CVS BINs table
  await pool.query(`
    CREATE TABLE IF NOT EXISTS cvs_managed_bins (
      bin VARCHAR(10) PRIMARY KEY,
      description TEXT,
      created_at TIMESTAMPTZ DEFAULT NOW()
    )
  `);

  console.log('Inserting aberrant products...');

  let inserted = 0;
  for (const product of aberrantProducts) {
    try {
      await pool.query(`
        INSERT INTO cvs_aberrant_products (product_name, ndc)
        VALUES ($1, $2)
        ON CONFLICT (ndc) DO UPDATE SET product_name = $1
      `, [product.name, product.ndc.replace(/-/g, '')]);
      inserted++;
    } catch (err) {
      // Skip duplicates
    }
  }

  console.log(`Inserted ${inserted} aberrant products`);

  // Insert CVS BINs
  console.log('Inserting CVS BINs...');
  for (const bin of cvsBins) {
    await pool.query(`
      INSERT INTO cvs_managed_bins (bin, description)
      VALUES ($1, 'CVS Caremark managed BIN')
      ON CONFLICT (bin) DO NOTHING
    `, [bin]);
  }

  console.log(`Inserted ${cvsBins.length} CVS BINs`);

  await pool.end();
  console.log('Done!');
}

